function draw(){
    background(0);
    camerasettings();
    basicmovement();

}

function basicmovement(){
    // CHECK FOR GROUND WHEN W
    if(kb.pressing("W")){
        player.vel.y += 10;
    }

    if(kb.pressing("D")){
        player.x += base;
    }

    if(kb.pressing("A")){
        player.x -= base;
    }
}

function camerasettings(){

    camera.x = player.x;
    camera.y = player.y;
    camera.zoom = 4;

}